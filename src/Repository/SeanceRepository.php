    public function findWithExercises(int $id): ?Seance
    {
        return $this->createQueryBuilder('s')
            ->leftJoin('s.exercises', 'e') // On joint les exercices
            ->addSelect('e')               // On demande Ã  Doctrine de les hydrater
            ->where('s.id = :id')
            ->setParameter('id', $id)
            ->getQuery()
            ->getOneOrNullResult();
